package com.itheima.operator;

public class OperatorDemo7 {
    public static void main(String[] args) {
        // 自增自减
        int a = 0;
        a++; // a = a + 1;
        a++; // a = a + 1;
        a--; // a = a - 1;
        a--; // a = a - 1;
        a--; // a = a - 1;
        a--; // a = a - 1;
        System.out.println(a);
    }

}
